int	test(t_printf f)
{
	const char	**fmt;
	int			i;

	i = 0;
	fmt = g_test_d - 1;
	while (*++fmt)
	{
		f("case #%d - format: %s {\n\tzero: ", ++i, *fmt);
		f(*fmt, 0);
		f(",\n\tpositive: ");
		f(*fmt, 42424242);
		f(",\n\tnegative: ");
		f(*fmt, -42424242);
		f(",\n} end case #%d (%s)\n\n", i, *fmt);
	}
	return (0);
}

int	main(int argc, char **argv)
{
	(void) argv;
	if (argc < 2)
		return (test(&ft_printf));
	return (test(&printf));
}
